    public VisualizaTicket() {
        initComponents();
        
        DAOTicket dadosTicket = new DAOTicket();
        ArrayList<Ticket> listaTickets = new ArrayList();
        listaTickets = dadosTicket.selecionarTodosRegistros();
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaTicket.getModel();
        for(Ticket ticket : listaTickets)
        {
            Object[] dados = {
                ticket.getAssunto(), 
                ticket.getSetor(),
                ticket.getStatus(),
                ticket.getTipo_ocorr(),
                ticket.getDt_previsao(),
                ticket.getPrioridade(),
                ticket.getSolicitante(),
                ticket.getResponsavel(),
                ticket.getConteudo()};
            modelo.addRow(dados);
        }
    }

