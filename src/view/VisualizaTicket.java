    private void tabelaTicketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaTicketMouseClicked
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar esse registro?");
        if(resposta == 0)
        {
            
            Ticket ticket = new Ticket();
            int linha = tabelaTicket.getSelectedRow();
            ticket.setId(Integer.parseInt(tabelaTicket.getValueAt(linha, 0).toString()));
            ticket.setAssunto(tabelaTicket.getValueAt(linha, 1).toString());
            ticket.setSetor(tabelaTicket.getValueAt(linha, 2).toString());
            ticket.setStatus(tabelaTicket.getValueAt(linha, 3).toString());
            ticket.setTipo_ocorr(tabelaTicket.getValueAt(linha, 4).toString());
            ticket.setDt_previsao(tabelaTicket.getValueAt(linha, 5).toString());
            ticket.setPrioridade(tabelaTicket.getValueAt(linha, 6).toString());
            ticket.setSolicitante(tabelaTicket.getValueAt(linha, 7).toString());
            ticket.setResponsavel(tabelaTicket.getValueAt(linha, 8).toString());
            ticket.setConteudo(tabelaTicket.getValueAt(linha, 9).toString());
            
            
            
            new AlterarTicket(ticket).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_tabelaTicketMouseClicked

